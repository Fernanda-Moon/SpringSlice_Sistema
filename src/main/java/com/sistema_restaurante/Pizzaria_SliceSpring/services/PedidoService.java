package com.sistema_restaurante.Pizzaria_SliceSpring.services;

import com.sistema_restaurante.Pizzaria_SliceSpring.entities.Pedido;
import com.sistema_restaurante.Pizzaria_SliceSpring.repositories.PedidoRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import java.util.List;

@Service
public class PedidoService {

    @Autowired
    private PedidoRepository pedidoRepository;

    public List<Pedido> findAll() {
        return pedidoRepository.findAll();
    }

    public Pedido save(Pedido pedido) {
        return pedidoRepository.save(pedido);
    }

    public void deleteById(Long id) {
        pedidoRepository.deleteById(id);
    }
}
